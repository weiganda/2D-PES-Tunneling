def main():
    """
    Entry point: orchestrate the full 2D WKB tunnelling analysis.

    Execution flow:
      1. Parse CLI arguments and set up physical parameters.
      2. Load the 2D PES and build the spline interpolant.
      3. Locate canonical and tautomeric minima.
      4. Estimate the saddle point.
      5. Compute the MEP via the string method.
      6. Compute forward and reverse MAP paths (self-consistent).
      7. Evaluate κ(T) for MEP and MAP at the reporting temperature.
      8. Compute Eyring rate constants.
      9. Generate diagnostic plots (paths, transmission, action, Arrhenius).
     10. Write a CSV of rates vs. temperature and print a summary.
    """
    args = parse_args()
    grid_dir = args.grid_dir
    T_REPORT = args.T_REPORT
    mass = M_PROTON * args.mass_factor
    n_inst_nodes = args.n_inst_nodes
    n_workers = args.n_workers

    print("=" * 60)
    print("  2D WKB Tunnelling Correction Calculator")
    print("  (MEP AND MAP)")
    print("=" * 60)
    if n_workers > 1:
        print(f"  Parallelisation: {n_workers} workers")

    # --------------------------------------------------------
    # Load PES
    # --------------------------------------------------------
    print(f"\nLoading 2D PES from {grid_dir}/...")
    d1, d2, energies_raw, energies_smooth, spline = load_pes(grid_dir, args.smooth)
    print(f"  Grid: {len(d1)} x {len(d2)} = {len(d1)*len(d2)} points")
    print(f"  d1 range: {d1[0]:.3f} – {d1[-1]:.3f} Å")
    print(f"  d2 range: {d2[0]:.3f} – {d2[-1]:.3f} Å")
    print(f"  Raw energy range: {np.min(energies_raw):.4f} – {np.max(energies_raw):.4f} eV")

    # --------------------------------------------------------
    # Find true minima
    # --------------------------------------------------------
    # Initial guesses for the minima: canonical is near the short-distance
    # corner (small d₁, small d₂); tautomeric is near the long-distance corner.
    print("\nFinding canonical minimum...")
    canon_guess = [d1[0] + 0.1, d2[0] + 0.1]
    canonical_pos, E_canonical = find_minimum(spline, d1, d2, canon_guess)
    print(f"  Canonical minimum at d1 = {canonical_pos[0]:.4f}, d2 = {canonical_pos[1]:.4f} Å")
    print(f"  E(canonical) = {E_canonical:.4f} eV")

    print("\nFinding tautomeric minimum...")
    taut_guess = [d1[-1] - 0.1, d2[-1] - 0.1]
