    print(f"\n  Tautomeric energy (relative) = {DE_taut:.4f} eV")

    # --------------------------------------------------------
    # Find saddle point
    # --------------------------------------------------------
    print("\nSearching for saddle point...")
    saddle_pos, E_saddle = find_saddle_point(spline, d1, d2, canonical_pos, tautomeric_pos)
    DV1 = E_saddle - E_ref
    DV2 = E_saddle - E_tautomeric
    DE = E_tautomeric - E_ref

    print(f"  Saddle point at d1 = {saddle_pos[0]:.4f}, d2 = {saddle_pos[1]:.4f} Å")
    print(f"  E(saddle) = {E_saddle:.4f} eV")
    print(f"\n  Forward barrier  ΔV₁ = {DV1:.4f} eV")
    print(f"  Reverse barrier  ΔV₂ = {DV2:.4f} eV")
    print(f"  Asymmetry        ΔE  = {DE:.4f} eV")

