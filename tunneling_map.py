    plt.colorbar(contour, ax=ax, label='Energy (eV)')
    ax.set_xlabel(r'$d_1$ (Å)')
    ax.set_ylabel(r'$d_2$ (Å)')
    ax.set_title('2D PES with MEP')
    ax.legend(fontsize=8)

    ax = axes[1]
    ax.plot(mep_arc, mep_energies_rel, 'b-', lw=2)
    ax.axhline(0, color='gray', lw=0.8, ls='--')
    ax.axhline(mep_final_DV1, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₁ = {mep_final_DV1:.3f} eV')
    ax.set_xlabel('Path Length MEP (Å)')
    ax.set_ylabel('Energy (eV)')
