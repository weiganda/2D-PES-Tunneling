    ax.legend(fontsize=9)

    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_mep_analysis.png', dpi=150)
    plt.close()
    print(f"\nSaved: {grid_dir}/2d_mep_analysis.png")

    # --------------------------------------------------------
    # Find MAP — forward (self-consistent, MEP-free)
    # --------------------------------------------------------
    print(f"\nSearching for forward MAP ({n_inst_nodes} free nodes)...")
    (inst_path_f, inst_nodes_f, inst_DV1, inst_curv_f,
     inst_label_f, inst_conv_f) = find_map_self_consistent(
        spline, d1, d2, canonical_pos, tautomeric_pos,
        E_canonical, E_tautomeric, mass,
        n_free_nodes=n_inst_nodes,
        scf_tol=args.map_scf_tol,
        scf_maxiter=args.map_scf_maxiter,
        label="forward",
        mep_path=mep_path
    )
    inst_DV1_check, inst_DV2_from_fwd, _, _ = get_path_barriers(
        inst_path_f, spline, E_canonical, E_tautomeric
    )

    print(f"  Forward MAP:")
    print(f"    ΔV₁(MAP_f) = {inst_DV1:.4f} eV")
    print(f"    ΔV₂ seen by forward path = {inst_DV2_from_fwd:.4f} eV")
    print(f"    Path curvature: {inst_curv_f:.4f} Å")
    print(f"    Best initialisation: {inst_label_f}")

    # --------------------------------------------------------
    # Find MAP — reverse (self-consistent, MEP-free)
    # --------------------------------------------------------
    print(f"\nSearching for reverse MAP ({n_inst_nodes} free nodes)...")
    (inst_path_r, inst_nodes_r, inst_DV2, inst_curv_r,
     inst_label_r, inst_conv_r) = find_map_self_consistent(
        spline, d1, d2, canonical_pos, tautomeric_pos,
        E_canonical, E_tautomeric, mass,
        n_free_nodes=n_inst_nodes,
        scf_tol=args.map_scf_tol,
        scf_maxiter=args.map_scf_maxiter,
        label="reverse",
        mep_path=mep_path
    )
    inst_DV1_from_rev, inst_DV2_check, _, _ = get_path_barriers(
        inst_path_r, spline, E_canonical, E_tautomeric
    )

    print(f"  Reverse MAP:")
    print(f"    ΔV₂(MAP_r) = {inst_DV2:.4f} eV")
    print(f"    ΔV₁ seen by reverse path = {inst_DV1_from_rev:.4f} eV")
    print(f"    Path curvature: {inst_curv_r:.4f} Å")
    print(f"    Best initialisation: {inst_label_r}")

    # --------------------------------------------------------
    # Report path differences
    # --------------------------------------------------------
    path_diff = np.mean(np.sqrt(np.sum((inst_path_f - inst_path_r)**2, axis=1)))
    print(f"\n  Mean path separation (MAP_f vs MAP_r): {path_diff:.4f} Å")
    if path_diff < 0.001:
        print(f"  -> Forward and reverse MAP paths are essentially identical")
    else:
        print(f"  -> Forward and reverse MAP paths differ")

    # --------------------------------------------------------
    # WKB κ using MEP — forward and reverse independently
    # --------------------------------------------------------
    print(f"\nComputing WKB κ (MEP, forward) at T = {T_REPORT:.1f} K...")
    kappa_mep_f, E_grid_mep_f, T_mep_f = wkb_kappa_2d(
        T_REPORT, spline, d1, d2, mass, mep_final_DV1,
        E_canonical, mep_path
    )
    print(f"  κ_MEP(forward) = {kappa_mep_f:.4f}")

    print(f"\nComputing WKB κ (MEP, reverse) at T = {T_REPORT:.1f} K...")
    kappa_mep_r, E_grid_mep_r, T_mep_r = wkb_kappa_2d(
        T_REPORT, spline, d1, d2, mass, mep_final_DV2,
        E_tautomeric, mep_path
    )
    print(f"  κ_MEP(reverse) = {kappa_mep_r:.4f}")

    # --------------------------------------------------------
    # WKB κ using MAP — forward (own path, own barrier)
    # --------------------------------------------------------
    print(f"\nComputing WKB κ_f (MAP forward) at T = {T_REPORT:.1f} K...")
    kappa_inst_f, E_grid_inst_f, T_inst_f = wkb_kappa_2d_instanton(
        T_REPORT, spline, d1, d2, mass, inst_DV1, E_canonical,
        canonical_pos, tautomeric_pos, n_inst_nodes,
        n_workers=n_workers, energies_smooth=energies_smooth,
        mep_path=mep_path
    )
    print(f"  κ_MAP(forward) = {kappa_inst_f:.4f}")

    # --------------------------------------------------------
    # WKB κ using MAP — reverse (own path, own barrier)
    # --------------------------------------------------------
    print(f"\nComputing WKB κ_r (MAP reverse) at T = {T_REPORT:.1f} K...")
    kappa_inst_r, E_grid_inst_r, T_inst_r = wkb_kappa_2d_instanton(
        T_REPORT, spline, d1, d2, mass, inst_DV2, E_tautomeric,
        canonical_pos, tautomeric_pos, n_inst_nodes,
        n_workers=n_workers, energies_smooth=energies_smooth,
        mep_path=mep_path
    )
    print(f"  κ_MAP(reverse) = {kappa_inst_r:.4f}")

    # --------------------------------------------------------
    # Eyring rates
    # --------------------------------------------------------
    # MEP: direction-specific κ, MEP barriers
    k_f_mep = eyring_rate(kappa_mep_f, T_REPORT, mep_final_DV1)
    k_r_mep = eyring_rate(kappa_mep_r, T_REPORT, mep_final_DV2)

    # MAP: direction-specific κ, direction-specific barriers
    k_f_inst = eyring_rate(kappa_inst_f, T_REPORT, inst_DV1)
    k_r_inst = eyring_rate(kappa_inst_r, T_REPORT, inst_DV2)

    print(f"\n{'='*60}")
    print(f"  Results at T = {T_REPORT:.2f} K")
    print(f"{'='*60}")
    print(f"\n  [MEP]  (barriers: ΔV₁ = {mep_final_DV1:.4f}, ΔV₂ = {mep_final_DV2:.4f} eV)")
    print(f"    κ_f       = {kappa_mep_f:.4f}")
    print(f"    κ_r       = {kappa_mep_r:.4f}")
    print(f"    k_f       = {k_f_mep:.4e} s⁻¹")
    print(f"    k_r       = {k_r_mep:.4e} s⁻¹")
    print(f"    τ_f       = {1/k_f_mep:.4e} s")
    print(f"    τ_r       = {1/k_r_mep:.4e} s")
    print(f"\n  [MAP forward]  (barrier: ΔV₁ = {inst_DV1:.4f} eV)")
    print(f"    κ_f       = {kappa_inst_f:.4f}")
    print(f"    k_f       = {k_f_inst:.4e} s⁻¹")
    print(f"    τ_f       = {1/k_f_inst:.4e} s")
    print(f"\n  [MAP reverse]  (barrier: ΔV₂ = {inst_DV2:.4f} eV)")
    print(f"    κ_r       = {kappa_inst_r:.4f}")
    print(f"    k_r       = {k_r_inst:.4e} s⁻¹")
    print(f"    τ_r       = {1/k_r_inst:.4e} s")

    # --------------------------------------------------------
    # Transmission plots — Forward and Reverse
    # --------------------------------------------------------
    beta_plot = 1.0 / (KB * T_REPORT)

    # (1) Forward: MEP vs MAP
    T_boltz_mep_f = T_mep_f * np.exp(-beta_plot * E_grid_mep_f)
    T_boltz_inst_f = T_inst_f * np.exp(-beta_plot * E_grid_inst_f)

    fig, ax = plt.subplots(figsize=(7, 5))
    ax2 = ax.twinx()
    l1, = ax.semilogy(E_grid_mep_f, T_mep_f, color='#c85000', lw=2,
                      label='T(E) — MEP')
    l2, = ax.semilogy(E_grid_inst_f, T_inst_f, color='#0055c8', lw=2,
                      ls='--', label='T(E) — MAP')
    l3, = ax2.semilogy(E_grid_mep_f, T_boltz_mep_f, color='#c85000',
                       lw=1.5, ls=':', alpha=0.7,
                       label='T(E)·exp(−βE) — MEP')
    l4, = ax2.semilogy(E_grid_inst_f, T_boltz_inst_f, color='#0055c8',
                       lw=1.5, ls=':', alpha=0.7,
                       label='T(E)·exp(−βE) — MAP')
    ax.axvline(mep_final_DV1, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₁(MEP) = {mep_final_DV1:.3f} eV')
    ax.axvline(inst_DV1, color='#0055c8', lw=0.8, ls=':',
               label=f'ΔV₁(MAP) = {inst_DV1:.3f} eV')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel('Tunnelling probability T(E)')
    ax2.set_ylabel('Tunnelling factor T(E)·exp(−βE)')
    ax.set_title(f'Forward tunnelling: MEP vs MAP — T = {T_REPORT:.1f} K')
    ax.legend([l1, l2, l3, l4],
              [l1.get_label(), l2.get_label(), l3.get_label(), l4.get_label()],
              fontsize=8, loc='center left')
    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_WKB_Tunnelling_forward.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_WKB_Tunnelling_forward.png")

    # (2) Reverse: MEP vs MAP
    T_boltz_mep_r = T_mep_r * np.exp(-beta_plot * E_grid_mep_r)
    T_boltz_inst_r = T_inst_r * np.exp(-beta_plot * E_grid_inst_r)

    fig, ax = plt.subplots(figsize=(7, 5))
    ax2 = ax.twinx()
    l1, = ax.semilogy(E_grid_mep_r, T_mep_r, color='#c85000', lw=2,
                      label='T(E) — MEP')
    l2, = ax.semilogy(E_grid_inst_r, T_inst_r, color='#2ca02c', lw=2,
                      ls='--', label='T(E) — MAP')
    l3, = ax2.semilogy(E_grid_mep_r, T_boltz_mep_r, color='#c85000',
                       lw=1.5, ls=':', alpha=0.7,
                       label='T(E)·exp(−βE) — MEP')
    l4, = ax2.semilogy(E_grid_inst_r, T_boltz_inst_r, color='#2ca02c',
                       lw=1.5, ls=':', alpha=0.7,
                       label='T(E)·exp(−βE) — MAP')
    ax.axvline(mep_final_DV2, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₂(MEP) = {mep_final_DV2:.3f} eV')
    ax.axvline(inst_DV2, color='#2ca02c', lw=0.8, ls=':',
               label=f'ΔV₂(MAP) = {inst_DV2:.3f} eV')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel('Tunnelling probability T(E)')
    ax2.set_ylabel('Tunnelling factor T(E)·exp(−βE)')
    ax.set_title(f'Reverse tunnelling: MEP vs MAP — T = {T_REPORT:.1f} K')
    ax.legend([l1, l2, l3, l4],
              [l1.get_label(), l2.get_label(), l3.get_label(), l4.get_label()],
              fontsize=8, loc='center left')
    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_WKB_Tunnelling_reverse.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_WKB_Tunnelling_reverse.png")

    # --------------------------------------------------------
    # Action difference: ΔS(E) = S_MEP(E) - S_MAP(E)
    # Positive ΔS means the MAP has lower action (higher
    # transmission) at that energy — this is the regime where
    # corner-cutting matters.
    # --------------------------------------------------------
    print("\nComputing action difference ΔS(E) = S_MEP - S_MAP...")
    n_action_pts = 200

    # --- Forward action comparison ---
    E_action_grid_f = np.linspace(1e-6, max(mep_final_DV1, inst_DV1) * 1.2,
                                  n_action_pts)
    S_mep_f_arr = np.zeros(n_action_pts)
    S_inst_f_arr = np.zeros(n_action_pts)
    for i, E in enumerate(E_action_grid_f):
        S_mep_f_arr[i] = compute_action(mep_path, spline, E + E_canonical, mass)
        S_inst_f_arr[i] = compute_action(inst_path_f, spline, E + E_canonical, mass)
    delta_S_f = S_mep_f_arr - S_inst_f_arr

    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    ax = axes[0]
    ax.plot(E_action_grid_f, S_mep_f_arr, color='#c85000', lw=2,
            label=r'$S_{\mathrm{MEP}}(E)$')
    ax.plot(E_action_grid_f, S_inst_f_arr, color='#0055c8', lw=2, ls='--',
            label=r'$S_{\mathrm{MAP}}(E)$')
    ax.axvline(mep_final_DV1, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₁(MEP) = {mep_final_DV1:.3f} eV')
    ax.axvline(inst_DV1, color='#0055c8', lw=0.8, ls=':',
               label=f'ΔV₁(MAP) = {inst_DV1:.3f} eV')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel('WKB action S(E)')
    ax.set_title('Forward: WKB action along MEP and MAP')
    ax.legend(fontsize=9)

    ax = axes[1]
    ax.plot(E_action_grid_f, delta_S_f, color='#0055c8', lw=2)
    ax.axhline(0, color='gray', lw=0.5, ls='-')
    ax.axvline(mep_final_DV1, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₁(MEP) = {mep_final_DV1:.3f} eV')
    ax.axvline(inst_DV1, color='#0055c8', lw=0.8, ls=':',
               label=f'ΔV₁(MAP) = {inst_DV1:.3f} eV')
    ax.fill_between(E_action_grid_f, 0, delta_S_f,
                    where=(delta_S_f > 0), alpha=0.3, color='#0055c8',
                    label='MAP favoured')
    ax.fill_between(E_action_grid_f, 0, delta_S_f,
                    where=(delta_S_f < 0), alpha=0.3, color='#c85000',
                    label='MEP favoured')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel(r'$\Delta S(E) = S_{\mathrm{MEP}} - S_{\mathrm{MAP}}$')
    ax.set_title('Forward: action advantage of MAP over MEP')
    ax.legend(fontsize=8)

    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_action_difference_fwd.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_action_difference_fwd.png")

    # --- Reverse action comparison ---
    E_action_grid_r = np.linspace(1e-6, max(mep_final_DV2, inst_DV2) * 1.2,
                                  n_action_pts)
    S_mep_r_arr = np.zeros(n_action_pts)
    S_inst_r_arr = np.zeros(n_action_pts)
    for i, E in enumerate(E_action_grid_r):
        S_mep_r_arr[i] = compute_action(mep_path, spline, E + E_tautomeric, mass)
        S_inst_r_arr[i] = compute_action(inst_path_r, spline, E + E_tautomeric, mass)
    delta_S_r = S_mep_r_arr - S_inst_r_arr

    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    ax = axes[0]
    ax.plot(E_action_grid_r, S_mep_r_arr, color='#c85000', lw=2,
            label=r'$S_{\mathrm{MEP}}(E)$')
    ax.plot(E_action_grid_r, S_inst_r_arr, color='#2ca02c', lw=2, ls='--',
            label=r'$S_{\mathrm{MAP}}(E)$')
    ax.axvline(mep_final_DV2, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₂(MEP) = {mep_final_DV2:.3f} eV')
    ax.axvline(inst_DV2, color='#2ca02c', lw=0.8, ls=':',
               label=f'ΔV₂(MAP) = {inst_DV2:.3f} eV')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel('WKB action S(E)')
    ax.set_title('Reverse: WKB action along MEP and MAP')
    ax.legend(fontsize=9)

    ax = axes[1]
    ax.plot(E_action_grid_r, delta_S_r, color='#2ca02c', lw=2)
    ax.axhline(0, color='gray', lw=0.5, ls='-')
    ax.axvline(mep_final_DV2, color='#c85000', lw=0.8, ls=':',
               label=f'ΔV₂(MEP) = {mep_final_DV2:.3f} eV')
    ax.axvline(inst_DV2, color='#2ca02c', lw=0.8, ls=':',
               label=f'ΔV₂(MAP) = {inst_DV2:.3f} eV')
    ax.fill_between(E_action_grid_r, 0, delta_S_r,
                    where=(delta_S_r > 0), alpha=0.3, color='#2ca02c',
                    label='MAP favoured')
    ax.fill_between(E_action_grid_r, 0, delta_S_r,
                    where=(delta_S_r < 0), alpha=0.3, color='#c85000',
                    label='MEP favoured')
    ax.set_xlabel('Energy (eV)')
    ax.set_ylabel(r'$\Delta S(E) = S_{\mathrm{MEP}} - S_{\mathrm{MAP}}$')
    ax.set_title('Reverse: action advantage of MAP over MEP')
    ax.legend(fontsize=8)

    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_action_difference_rev.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_action_difference_rev.png")

    # --------------------------------------------------------
    # Temperature dependence — MEP and MAP (independent paths)
    # --------------------------------------------------------
    print(f"\nCalculating rates over {T_RANGE[0]}–{T_RANGE[1]} K...")
    T_arr = np.linspace(T_RANGE[0], T_RANGE[1], N_TEMPS)

    if n_workers > 1:
        # --- Parallel temperature sweep ---
        print(f"  Dispatching {N_TEMPS} temperatures "
              f"across {n_workers} workers...")

        worker_args = [
            (T, d1, d2, energies_smooth, mass,
             mep_final_DV1, mep_final_DV2, E_canonical, E_tautomeric,
             mep_path, inst_path_f, inst_path_r,
             inst_DV1, inst_DV2)
            for T in T_arr
        ]

        with mp.Pool(processes=n_workers) as pool:
            results = pool.map(_temp_worker, worker_args)

        kap_mep_f_arr = np.array([r[0] for r in results])
        kap_mep_r_arr = np.array([r[1] for r in results])
        kf_mep_arr = np.array([r[2] for r in results])
        kr_mep_arr = np.array([r[3] for r in results])
        kap_inst_f_arr = np.array([r[4] for r in results])
        kap_inst_r_arr = np.array([r[5] for r in results])
        kf_inst_arr = np.array([r[6] for r in results])
        kr_inst_arr = np.array([r[7] for r in results])

        print(f"  All {N_TEMPS} temperatures complete.")

    else:
        # --- Serial temperature sweep ---
        kf_mep_arr = np.zeros(N_TEMPS)
        kr_mep_arr = np.zeros(N_TEMPS)
        kap_mep_f_arr = np.zeros(N_TEMPS)
        kap_mep_r_arr = np.zeros(N_TEMPS)
        kf_inst_arr = np.zeros(N_TEMPS)
        kr_inst_arr = np.zeros(N_TEMPS)
        kap_inst_f_arr = np.zeros(N_TEMPS)
        kap_inst_r_arr = np.zeros(N_TEMPS)

        for i, T in enumerate(T_arr):
            # MEP forward κ
            kap_mf, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                         mep_final_DV1, E_canonical,
                                         mep_path, n_energies=300)
            # MEP reverse κ
            kap_mr, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                         mep_final_DV2, E_tautomeric,
                                         mep_path, n_energies=300)
            kap_mep_f_arr[i] = kap_mf
            kap_mep_r_arr[i] = kap_mr
            kf_mep_arr[i] = eyring_rate(kap_mf, T, mep_final_DV1)
            kr_mep_arr[i] = eyring_rate(kap_mr, T, mep_final_DV2)

            # MAP forward
            kap_if, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                         inst_DV1, E_canonical,
                                         inst_path_f, n_energies=300)
            kap_inst_f_arr[i] = kap_if
            kf_inst_arr[i] = eyring_rate(kap_if, T, inst_DV1)

            # MAP reverse
            kap_ir, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                         inst_DV2, E_tautomeric,
                                         inst_path_r, n_energies=300)
            kap_inst_r_arr[i] = kap_ir
            kr_inst_arr[i] = eyring_rate(kap_ir, T, inst_DV2)

            if (i + 1) % 20 == 0:
                print(f"  {i+1}/{N_TEMPS} temperatures done...")

    # Arrhenius plots — Forward and Reverse
    inv_T = 1000.0 / T_arr

    # (1) Forward: MEP vs MAP
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.semilogy(inv_T, kf_mep_arr, color='#c85000', lw=2,
                label=r'$k_f$ MEP')
    ax.semilogy(inv_T, kf_inst_arr, color='#0055c8', lw=2, ls='--',
                label=r'$k_f$ MAP')
    ax.axvline(1000.0 / T_REPORT, color='gray', lw=0.8, ls=':',
               label=f'T = {T_REPORT:.0f} K')
    ax.set_xlabel('1000 / T  (K⁻¹)')
    ax.set_ylabel('Rate constant  (s⁻¹)')
    ax.set_title('Forward Eyring rates: MEP vs MAP')
    ax.legend(fontsize=8)
    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_WKB_eyring_rates_fwd.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_WKB_eyring_rates_fwd.png")

    # (2) Reverse: MEP vs MAP
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.semilogy(inv_T, kr_mep_arr, color='#c85000', lw=2,
                label=r'$k_r$ MEP')
    ax.semilogy(inv_T, kr_inst_arr, color='#2ca02c', lw=2, ls='--',
                label=r'$k_r$ MAP')
    ax.axvline(1000.0 / T_REPORT, color='gray', lw=0.8, ls=':',
               label=f'T = {T_REPORT:.0f} K')
    ax.set_xlabel('1000 / T  (K⁻¹)')
    ax.set_ylabel('Rate constant  (s⁻¹)')
    ax.set_title('Reverse Eyring rates: MEP vs MAP')
    ax.legend(fontsize=8)
    plt.tight_layout()
    plt.savefig(f'{grid_dir}/2d_WKB_eyring_rates_rev.png', dpi=150)
    plt.close()
    print(f"Saved: {grid_dir}/2d_WKB_eyring_rates_rev.png")

    # --------------------------------------------------------
    # MEP vs MAP paths plot
    # --------------------------------------------------------
    fig, ax = plt.subplots(figsize=(8, 7))
    levels = np.linspace(0, min(np.max(energies_raw), 2.0), 30)
    contour = ax.contourf(D1, D2, energies_raw, levels=levels, cmap='viridis')
    ax.contour(D1, D2, energies_raw, levels=levels, colors='black',
               linewidths=0.3, alpha=0.5)
