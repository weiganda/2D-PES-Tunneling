# 1. LOAD AND INTERPOLATE 2D PES
# ============================================================
def load_pes(grid_dir, smooth_sigma=0.3):
    """
    Load the 2D PES from NumPy arrays and build a smooth interpolant.

    Optional Gaussian smoothing (controlled by ``smooth_sigma``) reduces
    noise from incomplete SCF convergence or sparse grid regions without
    shifting the locations of stationary points appreciably.

    Parameters
    ----------
    grid_dir : str
        Directory containing ``d1_values.npy``, ``d2_values.npy``, and
        ``energies_eV.npy`` (produced by ``generate_2d_pes.py collect``).
    smooth_sigma : float
        Standard deviation (in grid-point units) for the Gaussian filter.
        Set to 0 to skip smoothing entirely.

    Returns
    -------
    d1, d2 : ndarray
        1-D scan coordinate arrays (Å).
    energies : ndarray
        Raw (unsmoothed) 2D energy surface (eV).
    energies_smooth : ndarray
        Smoothed energy surface (eV).
    spline : RectBivariateSpline
        Bicubic spline interpolant of the smoothed surface.
    """
    d1 = np.load(f"{grid_dir}/d1_values.npy")
    d2 = np.load(f"{grid_dir}/d2_values.npy")
    energies = np.load(f"{grid_dir}/energies_eV.npy")

    if smooth_sigma > 0:
        energies_smooth = gaussian_filter(energies, sigma=smooth_sigma)
    else:
        energies_smooth = energies.copy()

    spline = interpolate.RectBivariateSpline(d1, d2, energies_smooth, kx=3, ky=3)

    return d1, d2, energies, energies_smooth, spline


def _rebuild_spline(d1, d2, energies_smooth):
    """
    Reconstruct the bicubic spline from raw arrays.

    This exists because ``RectBivariateSpline`` objects cannot be pickled
    for ``multiprocessing``.  Each worker process calls this function to
    build its own local spline from the serialisable NumPy arrays.
    """
    return interpolate.RectBivariateSpline(d1, d2, energies_smooth, kx=3, ky=3)


def get_energy(spline, x1, x2):
    """
    Evaluate the interpolated potential energy at a single (d₁, d₂) point.

    Wrapper around ``RectBivariateSpline.__call__`` with ``grid=False``
    to return a scalar rather than a 2D array.
    """
    return float(spline(x1, x2, grid=False))


# ============================================================
# 2. FIND CANONICAL AND TAUTOMER MINIMAS
#       FIND SADDLE POINT FOR MEP
# ============================================================
def find_minimum(spline, d1, d2, initial_guess, label="minimum"):
    """
    Locate a local minimum on the interpolated 2D surface.

    Uses L-BFGS-B with bounds slightly inset from the grid edges to
    prevent the optimiser from running off the spline domain.

    Parameters
    ----------
    spline : RectBivariateSpline
        Interpolated PES.
    d1, d2 : ndarray
        1-D coordinate arrays (used only to set bounds).
    initial_guess : array-like, shape (2,)
        Starting (d₁, d₂) for the search.
    label : str
        Descriptive name (for logging only).

    Returns
    -------
    position : ndarray, shape (2,)
        Optimised (d₁, d₂) of the minimum.
    energy : float
        Energy at the minimum (eV).
    """
    bounds = [(d1[0] + 0.01, d1[-1] - 0.01),
              (d2[0] + 0.01, d2[-1] - 0.01)]

    result = optimize.minimize(
        lambda xy: get_energy(spline, xy[0], xy[1]),
        initial_guess,
        method='L-BFGS-B',
        bounds=bounds
    )

    if result.success:
        return result.x, result.fun
    else:
        E = get_energy(spline, initial_guess[0], initial_guess[1])
        return np.array(initial_guess), E


def find_saddle_point(spline, d1, d2, canonical_pos, tautomeric_pos, n_search=200):
    """
    Estimate the transition-state saddle point between two minima.

    Strategy: generate a family of curved paths connecting the canonical
    and tautomeric minima (parameterised by a scalar curvature offset
    along the perpendicular direction).  For each path, find the energy
    maximum.  The saddle point is the *lowest* such maximum across all
    paths — i.e., the minimax point.

    This is a heuristic search, not a true saddle-point optimisation
    (e.g., dimer or partitioned rational function).  It works well on
    smooth 2D surfaces with a single transition state between two minima.

    Parameters
    ----------
    spline : RectBivariateSpline
        Interpolated PES.
    d1, d2 : ndarray
        1-D coordinate arrays (used for boundary checking).
    canonical_pos, tautomeric_pos : ndarray, shape (2,)
        Positions of the two minima.
    n_search : int
        Number of curvature values to scan (more = finer search).

    Returns
    -------
    saddle_pos : ndarray, shape (2,)
        Estimated saddle-point coordinates.
    saddle_E : float
        Energy at the saddle point (eV).
    """
    best_saddle_E = np.inf
    best_saddle_pos = None

    curvatures = np.linspace(-0.4, 0.4, n_search)
    start = canonical_pos
    end = tautomeric_pos
    perp = np.array([-(end[1] - start[1]), end[0] - start[0]])
    perp = perp / np.linalg.norm(perp)

    for curv in curvatures:
        t_vals = np.linspace(0, 1, 200)
        path = np.array([
            start + t * (end - start) + curv * np.sin(np.pi * t) * perp
            for t in t_vals
        ])

        if (path[:, 0].min() < d1[0] or path[:, 0].max() > d1[-1] or
            path[:, 1].min() < d2[0] or path[:, 1].max() > d2[-1]):
            continue

        energies_path = np.array([get_energy(spline, p[0], p[1]) for p in path])
        max_idx = np.argmax(energies_path)
        max_E = energies_path[max_idx]

        if max_E < best_saddle_E:
            best_saddle_E = max_E
            best_saddle_pos = path[max_idx]

    return best_saddle_pos, best_saddle_E


# ============================================================
# 3. FIND MINIMUM ENERGY PATH (MEP) — STRING METHOD
# ============================================================
def find_mep(spline, d1, d2, canonical_pos, tautomeric_pos, n_images=100):
    """
    Find the Minimum Energy Path (MEP) using the simplified string method.

    The string method represents the path as a chain of images
    (discretisation points) and evolves them by:

      1. Computing the energy gradient at each interior image.
      2. Projecting out the component along the path tangent (so images
         move only perpendicular to the path, toward lower energy).
      3. Redistributing images at equal arc-length spacing to prevent
         them from clustering near the minima.

    Iteration continues until the maximum perpendicular step falls
    below 10⁻⁶ Å (or 500 iterations, whichever comes first).

    Parameters
    ----------
    spline : RectBivariateSpline
        Interpolated PES.
    d1, d2 : ndarray
        1-D coordinate arrays (for boundary clipping).
    canonical_pos, tautomeric_pos : ndarray, shape (2,)
        Fixed endpoints of the path.
    n_images : int
        Number of discretisation points along the path.

    Returns
    -------
    path : ndarray, shape (n_images, 2)
        Converged MEP coordinates.
    energies_along : ndarray, shape (n_images,)
        Energy at each image (eV).
    arc_lengths : ndarray, shape (n_images,)
        Cumulative arc length from the first image (Å).
    """
    start = canonical_pos.copy()
    end = tautomeric_pos.copy()

    path = np.array([start + t * (end - start)
                     for t in np.linspace(0, 1, n_images)])

    ds = 0.001
    for iteration in range(500):
        new_path = path.copy()
        max_move = 0

        for i in range(1, n_images - 1):
            tangent = path[i+1] - path[i-1]
            norm = np.linalg.norm(tangent)
            if norm < 1e-10:
                continue
            tangent = tangent / norm

            E0 = get_energy(spline, path[i, 0], path[i, 1])
            dEdx1 = (get_energy(spline, path[i, 0] + ds, path[i, 1]) - E0) / ds
            dEdx2 = (get_energy(spline, path[i, 0], path[i, 1] + ds) - E0) / ds
            grad = np.array([dEdx1, dEdx2])

            grad_perp = grad - np.dot(grad, tangent) * tangent
            step = 0.003 * grad_perp
            new_path[i] -= step
            max_move = max(max_move, np.linalg.norm(step))

            new_path[i, 0] = np.clip(new_path[i, 0], d1[0] + 0.01, d1[-1] - 0.01)
            new_path[i, 1] = np.clip(new_path[i, 1], d2[0] + 0.01, d2[-1] - 0.01)

        # Redistribute evenly
        diffs = np.diff(new_path, axis=0)
        seg_lengths = np.sqrt(np.sum(diffs**2, axis=1))
        cum_lengths = np.concatenate([[0], np.cumsum(seg_lengths)])
        total_length = cum_lengths[-1]

        if total_length < 1e-10:
            break

        even_s = np.linspace(0, total_length, n_images)
        path_x1 = np.interp(even_s, cum_lengths, new_path[:, 0])
        path_x2 = np.interp(even_s, cum_lengths, new_path[:, 1])
        path = np.column_stack([path_x1, path_x2])

        path[0] = start
        path[-1] = end

        if max_move < 1e-6 and iteration > 50:
            break

    energies_along = np.array([get_energy(spline, p[0], p[1]) for p in path])
    seg_lengths = np.sqrt(np.sum(np.diff(path, axis=0)**2, axis=1))
    arc_lengths = np.concatenate([[0], np.cumsum(seg_lengths)])

    return path, energies_along, arc_lengths


# ============================================================
# 4. MINIMAL ACTION PATH (MAP) CALCULATION
# ============================================================
def _nodes_to_path(nodes_flat, start, end, n_interp=200):
    """
    Convert a flat array of free-node coordinates into a dense path.

    The MAP path is parameterised by a small number of movable interior
    nodes (typically 24).  This function inserts the fixed endpoints,
    computes cumulative arc length along the node chain, and then
    linearly interpolates to produce a uniformly sampled dense path
    suitable for numerical integration of the WKB action.

    Parameters
    ----------
    nodes_flat : ndarray, shape (2*N_free,)
        Flattened coordinates [d1_1, d2_1, d1_2, d2_2, ...] of the
        free interior nodes.
    start, end : ndarray, shape (2,)
        Fixed path endpoints (canonical and tautomeric minima).
    n_interp : int
        Number of points in the output dense path.

    Returns
    -------
    ndarray, shape (n_interp, 2)
        Dense path coordinates.
    """
    n_free = len(nodes_flat) // 2
    all_nodes = np.zeros((n_free + 2, 2))
    all_nodes[0] = start
    all_nodes[-1] = end
    all_nodes[1:-1] = nodes_flat.reshape(n_free, 2)

    diffs = np.diff(all_nodes, axis=0)
    seg_lengths = np.sqrt(np.sum(diffs**2, axis=1))
    cum_s = np.concatenate([[0], np.cumsum(seg_lengths)])
    total_s = cum_s[-1]

    if total_s < 1e-12:
        return np.array([start + t * (end - start)
                         for t in np.linspace(0, 1, n_interp)])

    s_dense = np.linspace(0, total_s, n_interp)
    path_d1 = np.interp(s_dense, cum_s, all_nodes[:, 0])
    path_d2 = np.interp(s_dense, cum_s, all_nodes[:, 1])

    return np.column_stack([path_d1, path_d2])


def _action_objective(nodes_flat, start, end, spline, E_incident, mass,
                      d1_bounds, d2_bounds, penalty_weight=100.0):
    """
    Objective function for MAP optimisation: WKB action + boundary penalty.

    The WKB action integral is computed along the dense path defined by
    the free nodes.  A soft quadratic penalty is added for any node that
    strays outside the PES grid bounds, steering the optimiser back
    inward without hard constraints (which L-BFGS-B handles less
    efficiently for this problem).

    Parameters
    ----------
    nodes_flat : ndarray
        Flattened free-node coordinates (optimisation variables).
    start, end : ndarray
        Fixed endpoints.
    spline : RectBivariateSpline
        Interpolated PES.
    E_incident : float
        Incident (test) energy for the WKB integrand (eV).
    mass : float
        Effective tunnelling mass (eV·s²/Å²).
    d1_bounds, d2_bounds : tuple of (float, float)
        Allowed coordinate ranges.
    penalty_weight : float
        Stiffness of the boundary penalty.

    Returns
    -------
    float
        Action + penalty (dimensionless).
    """
    n_free = len(nodes_flat) // 2
    nodes = nodes_flat.reshape(n_free, 2)

    penalty = 0.0
    for nd in nodes:
        if nd[0] < d1_bounds[0]:
            penalty += penalty_weight * (d1_bounds[0] - nd[0])**2
        if nd[0] > d1_bounds[1]:
            penalty += penalty_weight * (nd[0] - d1_bounds[1])**2
        if nd[1] < d2_bounds[0]:
            penalty += penalty_weight * (d2_bounds[0] - nd[1])**2
        if nd[1] > d2_bounds[1]:
            penalty += penalty_weight * (nd[1] - d2_bounds[1])**2

    path = _nodes_to_path(nodes_flat, start, end, n_interp=200)
    action = compute_action(path, spline, E_incident, mass)

    return action + penalty


def find_instanton_variational(spline, d1, d2, canonical_pos, tautomeric_pos,
                                E_incident, mass, n_free_nodes=24,
                                mep_path=None):
    """
    Find the optimal tunnelling path by minimising the
    WKB action over all free node positions.

    The path is represented as n_free_nodes movable interior points
    between the fixed canonical and tautomeric endpoints. Each node
    has two degrees of freedom (d1, d2), giving 2*n_free_nodes total
    parameters. scipy.optimize.minimize (L-BFGS-B) is used to find
    the configuration that minimises the WKB action integral.

    Multiple initial conditions are tested:
      1. Straight-line (concerted diagonal)
      2. Two curved initialisations (biased toward each stepwise corner)
      3. MEP-seeded (if mep_path is provided)
    The best result across all initialisations is returned.
    """
    start = canonical_pos
    end = tautomeric_pos
    d1_bounds = (d1[0] + 0.005, d1[-1] - 0.005)
    d2_bounds = (d2[0] + 0.005, d2[-1] - 0.005)

    bounds = []
    for _ in range(n_free_nodes):
        bounds.append(d1_bounds)
        bounds.append(d2_bounds)

    initial_guesses = []

    t_nodes = np.linspace(0, 1, n_free_nodes + 2)[1:-1]
    straight_nodes = np.array([start + t * (end - start) for t in t_nodes])
    initial_guesses.append(('straight', straight_nodes.flatten()))

    perp = np.array([-(end[1] - start[1]), end[0] - start[0]])
    norm_perp = np.linalg.norm(perp)
    if norm_perp > 0:
        perp = perp / norm_perp
    for curv, label in [(0.15, 'curve_pos'), (-0.15, 'curve_neg')]:
        curved_nodes = np.array([
            start + t * (end - start) + curv * np.sin(np.pi * t) * perp
            for t in t_nodes
        ])
        curved_nodes[:, 0] = np.clip(curved_nodes[:, 0], d1_bounds[0], d1_bounds[1])
        curved_nodes[:, 1] = np.clip(curved_nodes[:, 1], d2_bounds[0], d2_bounds[1])
        initial_guesses.append((label, curved_nodes.flatten()))

    # 3. MEP-seeded initialisation (if provided)
    if mep_path is not None and len(mep_path) > 2:
        mep_diffs = np.diff(mep_path, axis=0)
        mep_seg = np.sqrt(np.sum(mep_diffs**2, axis=1))
        mep_cum = np.concatenate([[0], np.cumsum(mep_seg)])
        mep_total = mep_cum[-1]
        if mep_total > 1e-12:
            sample_s = np.linspace(0, mep_total, n_free_nodes + 2)[1:-1]
            mep_d1 = np.interp(sample_s, mep_cum, mep_path[:, 0])
            mep_d2 = np.interp(sample_s, mep_cum, mep_path[:, 1])
            mep_nodes = np.column_stack([mep_d1, mep_d2])
            mep_nodes[:, 0] = np.clip(mep_nodes[:, 0], d1_bounds[0], d1_bounds[1])
            mep_nodes[:, 1] = np.clip(mep_nodes[:, 1], d2_bounds[0], d2_bounds[1])
            initial_guesses.append(('mep', mep_nodes.flatten()))

    best_action = np.inf
    best_path = None
    best_label = None
    best_nodes = None

    for label, x0 in initial_guesses:
        try:
            result = optimize.minimize(
                _action_objective,
                x0,
                args=(start, end, spline, E_incident, mass, d1_bounds, d2_bounds),
                method='L-BFGS-B',
                bounds=bounds,
                options={'maxiter': 500, 'ftol': 1e-12}
            )

            if result.success or result.fun < best_action:
                path = _nodes_to_path(result.x, start, end, n_interp=200)
                action = compute_action(path, spline, E_incident, mass)

                if action < best_action:
                    best_action = action
                    best_path = path
                    best_label = label
                    best_nodes = result.x.reshape(-1, 2)
        except Exception:
            continue

    return best_path, best_action, best_label, best_nodes


def compute_path_curvature(path, canonical_pos, tautomeric_pos):
    """
    Compute a scalar curvature measure: the maximum perpendicular
    deviation of the path from the straight line connecting endpoints.
    Positive = toward high-d1/low-d2 corner; negative = opposite.
    """
    start = canonical_pos
    end = tautomeric_pos
    line_vec = end - start
    line_len = np.linalg.norm(line_vec)
    if line_len < 1e-10:
        return 0.0

    line_hat = line_vec / line_len
    perp_hat = np.array([-line_hat[1], line_hat[0]])

    max_dev = 0.0
    for p in path:
        delta = p - start
        perp_dist = np.dot(delta, perp_hat)
        if abs(perp_dist) > abs(max_dev):
            max_dev = perp_dist

    return max_dev


def get_path_barriers(path, spline, E_canonical, E_tautomeric):
    """
    Extract barrier heights from the energy profile along a given path.

    Returns:
        DV1_path: forward barrier (max energy along path - E_canonical)
        DV2_path: reverse barrier (max energy along path - E_tautomeric)
        E_max:    absolute energy at the path maximum
        max_idx:  index of the maximum along the path
    """
    energies = np.array([get_energy(spline, p[0], p[1]) for p in path])
    max_idx = np.argmax(energies)
    E_max = energies[max_idx]

    DV1_path = E_max - E_canonical
    DV2_path = E_max - E_tautomeric

    return DV1_path, DV2_path, E_max, max_idx


def find_map_self_consistent(spline, d1, d2, canonical_pos, tautomeric_pos,
                              E_canonical, E_tautomeric, mass,
                              n_free_nodes=24, scf_tol=1e-4,
                              scf_maxiter=10, label="forward",
                              mep_path=None):
    """
    Find the MAP path with a self-consistent barrier height.

    The MAP path depends on the incident energy used to seed the search,
    and the barrier height depends on the resulting path. This function
    iterates until the barrier used to set the test energy matches the
    barrier extracted from the converged path.

    For the forward direction, the barrier is referenced to E_canonical.
    For the reverse direction, the barrier is referenced to E_tautomeric.

    The initial guess for the barrier is obtained from a straight-line
    path between the two minima (no MEP dependence). The MEP geometry
    may be used as one of several initial guesses for the optimiser,
    but does not determine the barrier or constrain the result.

    Parameters:
        label: "forward" or "reverse" — determines which minimum is the
               reference for the barrier height
        mep_path: optional MEP path array to use as an additional
                  initial guess for the MAP optimiser

    Returns:
        best_path, best_nodes, DV, curvature, best_label, converged
    """
    if label == "forward":
        E_ref = E_canonical
    else:
        E_ref = E_tautomeric

    # --- Initial barrier guess from straight-line path (MEP-free) ---
    n_init = 200
    straight_path = np.array([
        canonical_pos + t * (tautomeric_pos - canonical_pos)
        for t in np.linspace(0, 1, n_init)
    ])
    straight_energies = np.array([
        get_energy(spline, p[0], p[1]) for p in straight_path
    ])
    DV_guess = np.max(straight_energies) - E_ref

    print(f"    SCF iteration 0: ΔV({label}) guess = {DV_guess:.4f} eV "
          f"(from straight-line path)")

    DV_current = DV_guess
    best_path = None
    best_nodes = None
    best_label_init = None
    converged = False
    delta = np.inf

    for scf_iter in range(1, scf_maxiter + 1):
        E_test = E_ref + DV_current * 0.5

        path, action, init_label, nodes = find_instanton_variational(
            spline, d1, d2, canonical_pos, tautomeric_pos,
            E_test, mass, n_free_nodes, mep_path=mep_path
        )

        if path is None:
            print(f"    SCF iteration {scf_iter}: MAP optimisation failed")
            break

        DV1_path, DV2_path, _, _ = get_path_barriers(
            path, spline, E_canonical, E_tautomeric
        )

        if label == "forward":
            DV_new = DV1_path
        else:
            DV_new = DV2_path

        delta = abs(DV_new - DV_current)
        print(f"    SCF iteration {scf_iter}: ΔV({label}) = {DV_new:.4f} eV "
              f"(Δ = {delta:.2e} eV, init: {init_label})")

        best_path = path
        best_nodes = nodes
        best_label_init = init_label
        DV_current = DV_new

        if delta < scf_tol:
            converged = True
            print(f"    SCF converged in {scf_iter} iterations")
            break

    if not converged:
        print(f"    SCF did not converge in {scf_maxiter} iterations "
              f"(final Δ = {delta:.2e} eV)")

    curvature = compute_path_curvature(best_path, canonical_pos, tautomeric_pos)

    return best_path, best_nodes, DV_current, curvature, best_label_init, converged


# ============================================================
# 5. WKB ACTION AND TRANSMISSION
# ============================================================
def compute_action(path_points, spline, E_incident, mass):
    """
    Compute the WKB action integral along a discretised path.

    The semiclassical (WKB) action for tunnelling through a barrier is:

        S = ∫ √(2m [V(s) − E] / ħ²) ds

    integrated only over the classically forbidden region where
    V(s) > E.  The integral is evaluated by the midpoint rule on
    consecutive path segments.

    Parameters
    ----------
    path_points : ndarray, shape (N, 2)
        Dense path in (d₁, d₂) space.
    spline : RectBivariateSpline
        Interpolated PES.
    E_incident : float
        Incident energy (eV), i.e. the energy of the tunnelling
        particle.  Segments where V < E_incident (classically allowed)
        contribute zero action.
    mass : float
        Effective tunnelling mass in eV·s²/Å².

    Returns
    -------
    float
        Dimensionless action (the exponent in T ≈ exp(−2S)).
    """
    action = 0.0
    for i in range(len(path_points) - 1):
        p1 = path_points[i]
        p2 = path_points[i + 1]
        mid = 0.5 * (p1 + p2)

        V = get_energy(spline, mid[0], mid[1])
        diff = V - E_incident

        if diff > 0:
            ds = np.sqrt((p2[0] - p1[0])**2 + (p2[1] - p1[1])**2)
            action += np.sqrt((2 * mass) / HBAR**2 * diff) * ds

    return action


def wkb_transmission_along_path(E, path, spline, mass):
    """
    WKB tunnelling transmission coefficient T(E) along a fixed path.

    T(E) = exp(−2S), where S is the action integral.  The result is
    clipped to [10⁻³⁰⁰, 1] to avoid numerical underflow/overflow.

    Parameters
    ----------
    E : float
        Incident energy (eV).
    path : ndarray, shape (N, 2)
        Fixed tunnelling path.
    spline : RectBivariateSpline
        Interpolated PES.
    mass : float
        Tunnelling mass (eV·s²/Å²).

    Returns
    -------
    float
        Transmission coefficient in [0, 1].
    """
    action = compute_action(path, spline, E, mass)
    return float(np.clip(np.exp(-2 * action), 1e-300, 1.0))


# ============================================================
# 6. KAPPA(T) INTEGRATION
# ============================================================
def wkb_kappa_2d(T_K, spline, d1, d2, mass, DV, E_ref, path,
                  n_energies=500):
    """
    Compute the tunnelling correction factor κ(T) along a fixed path.

    κ is the ratio of the quantum-mechanical to classical rate through
    the barrier.  It is computed by numerically integrating:

        κ(T) = β · exp(β·ΔV) · ∫₀^∞ T(E) · exp(−βE) dE

    where β = 1/(k_B T), ΔV is the classical barrier height, and T(E)
    is the WKB transmission probability evaluated along ``path``.

    The integration grid extends from near-zero to 2.5×ΔV, which is
    wide enough to capture the Boltzmann-weighted tail.  κ is floored
    at 1.0 (the classical limit — tunnelling can only enhance the rate).

    Parameters
    ----------
    T_K : float
        Temperature (K).
    spline : RectBivariateSpline
        Interpolated PES.
    d1, d2 : ndarray
        Coordinate arrays (passed through but unused here; kept for
        interface consistency).
    mass : float
        Tunnelling mass (eV·s²/Å²).
    DV : float
        Barrier height relative to ``E_ref`` (eV).
    E_ref : float
        Absolute energy of the reference minimum (eV).
    path : ndarray, shape (N, 2)
        Fixed tunnelling path.
    n_energies : int
        Number of quadrature points for the energy integral.

    Returns
    -------
    kappa : float
        Tunnelling correction factor (≥ 1.0).
    E_grid : ndarray
        Energy grid used for integration (eV, relative to E_ref).
    T_values : ndarray
        T(E) at each grid point.
    """
    beta = 1.0 / (KB * T_K)
    E_grid = np.linspace(1e-6, DV * 2.5, n_energies)

    T_values = np.zeros(n_energies)
    for i, E in enumerate(E_grid):
        T_values[i] = wkb_transmission_along_path(E + E_ref, path, spline, mass)

    integrand = T_values * np.exp(-beta * E_grid)
    kappa = beta * np.exp(beta * DV) * np.trapz(integrand, E_grid)

    return max(kappa, 1.0), E_grid, T_values


# --- Parallel worker for MAP kappa ---
def _instanton_worker(args_tuple):
    """
    Multiprocessing worker: optimise the MAP path at a single incident energy.

    Receives all data as plain arrays (picklable), rebuilds the spline
    locally, runs the MAP optimiser, and returns the transmission
    coefficient T(E) = exp(−2S).
    """
    (E_abs, d1, d2, energies_smooth, canonical_pos, tautomeric_pos,
     mass, n_free_nodes, mep_path) = args_tuple

    spline = _rebuild_spline(d1, d2, energies_smooth)

    best_path, best_action, _, _ = find_instanton_variational(
        spline, d1, d2, canonical_pos, tautomeric_pos,
        E_abs, mass, n_free_nodes, mep_path=mep_path
    )

    if best_path is not None:
        T_val = float(np.clip(np.exp(-2 * best_action), 1e-300, 1.0))
    else:
        T_val = 0.0

    return T_val


def wkb_kappa_2d_instanton(T_K, spline, d1, d2, mass, DV, E_ref,
                            canonical_pos, tautomeric_pos,
                            n_free_nodes=24, n_energies=300,
                            n_workers=1, energies_smooth=None,
                            mep_path=None):
    """
    Compute κ(T) with per-energy MAP path optimisation.

    Unlike ``wkb_kappa_2d``, which evaluates T(E) along a single
    pre-computed path, this function re-optimises the MAP at every
    incident energy in the integration grid.  This is the most rigorous
    treatment — the optimal tunnelling path depends on energy because
    the classically forbidden region changes shape — but it is far more
    expensive.

    Parallelism is supported: if ``n_workers > 1`` and
    ``energies_smooth`` is provided (so the spline can be rebuilt in
    each worker), the energy loop is distributed across a
    ``multiprocessing.Pool``.

    Parameters
    ----------
    T_K : float
        Temperature (K).
    spline : RectBivariateSpline
        Interpolated PES (used in serial mode).
    d1, d2 : ndarray
        Coordinate arrays.
    mass : float
        Tunnelling mass (eV·s²/Å²).
    DV : float
        Barrier height relative to E_ref (eV).
    E_ref : float
        Absolute energy of the reference minimum (eV).
    canonical_pos, tautomeric_pos : ndarray
        Path endpoints.
    n_free_nodes : int
        Number of free interior nodes in the MAP discretisation.
    n_energies : int
        Number of energy quadrature points.
    n_workers : int
        Number of parallel processes (1 = serial).
    energies_smooth : ndarray or None
        Smoothed energy surface (needed for parallel mode to rebuild
        the spline in each worker).
    mep_path : ndarray or None
        MEP path to use as an additional MAP seed.

    Returns
    -------
    kappa : float
        Tunnelling correction factor (≥ 1.0).
    E_grid : ndarray
        Energy integration grid (eV, relative to E_ref).
    T_values : ndarray
        T(E) at each grid point.
    """
    beta = 1.0 / (KB * T_K)
    E_grid = np.linspace(1e-6, DV * 2.5, n_energies)

    if n_workers > 1 and energies_smooth is not None:
        # --- Parallel execution ---
        worker_args = [
            (E + E_ref, d1, d2, energies_smooth,
             canonical_pos, tautomeric_pos, mass, n_free_nodes, mep_path)
            for E in E_grid
        ]

        print(f"    Dispatching {n_energies} energy points "
              f"across {n_workers} workers...")

        with mp.Pool(processes=n_workers) as pool:
            T_values = np.array(pool.map(_instanton_worker, worker_args))

        print(f"    All {n_energies} energy points complete.")

    else:
        # --- Serial execution ---
        T_values = np.zeros(n_energies)

        for i, E in enumerate(E_grid):
            best_path, best_action, _, _ = find_instanton_variational(
                spline, d1, d2, canonical_pos, tautomeric_pos,
                E + E_ref, mass, n_free_nodes, mep_path=mep_path
            )
            if best_path is not None:
                T_values[i] = float(np.clip(
                    np.exp(-2 * best_action), 1e-300, 1.0))

            if (i + 1) % 50 == 0:
                print(f"    MAP kappa: {i+1}/{n_energies} energies done...")

    integrand = T_values * np.exp(-beta * E_grid)
    kappa = beta * np.exp(beta * DV) * np.trapz(integrand, E_grid)

    return max(kappa, 1.0), E_grid, T_values


# --- Parallel worker for temperature sweep ---
def _temp_worker(args_tuple):
    """
    Multiprocessing worker for the temperature sweep.

    Computes all four κ values (MEP forward/reverse, MAP forward/reverse)
    and the corresponding Eyring rate constants at a single temperature.
    Returns an 8-tuple that is unpacked by the caller.
    """
    (T, d1, d2, energies_smooth, mass,
     mep_final_DV1, mep_final_DV2, E_canonical, E_tautomeric,
     mep_path, inst_path_f, inst_path_r,
     inst_DV1, inst_DV2) = args_tuple

    spline = _rebuild_spline(d1, d2, energies_smooth)

    # MEP forward κ
    kap_mep_f, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                    mep_final_DV1, E_canonical,
                                    mep_path, n_energies=300)
    # MEP reverse κ
    kap_mep_r, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                    mep_final_DV2, E_tautomeric,
                                    mep_path, n_energies=300)

    kf_mep = eyring_rate(kap_mep_f, T, mep_final_DV1)
    kr_mep = eyring_rate(kap_mep_r, T, mep_final_DV2)

    # MAP forward κ
    kap_if, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                 inst_DV1, E_canonical,
                                 inst_path_f, n_energies=300)
    kf_inst = eyring_rate(kap_if, T, inst_DV1)

    # MAP reverse κ
    kap_ir, _, _ = wkb_kappa_2d(T, spline, d1, d2, mass,
                                 inst_DV2, E_tautomeric,
                                 inst_path_r, n_energies=300)
    kr_inst = eyring_rate(kap_ir, T, inst_DV2)

    return (kap_mep_f, kap_mep_r, kf_mep, kr_mep,
            kap_if, kap_ir, kf_inst, kr_inst)


# ============================================================
# 7. EYRING RATE CONSTANTS
# ============================================================
def eyring_rate(kappa, T_K, delta_E_eV):
    """
    Compute the tunnelling-corrected Eyring (transition-state theory) rate.

    The Eyring equation gives the unimolecular rate constant:

        k = κ · (k_B T / h) · exp(−ΔE‡ / k_B T)

    where κ is the tunnelling correction factor, ΔE‡ is the electronic
    barrier height (no zero-point or thermal corrections), and the
    prefactor k_B T / h is the universal frequency factor from TST.

    Parameters
    ----------
    kappa : float
        Tunnelling correction factor (≥ 1).
    T_K : float
        Temperature (K).
    delta_E_eV : float
        Electronic barrier height (eV).

    Returns
    -------
    float
        Rate constant (s⁻¹).
    """
    prefactor = (KB_J * T_K) / H_J
    delta_E_J_mol = delta_E_eV * EV_TO_J_MOL
    boltzmann = np.exp(-delta_E_J_mol / (R_J * T_K))
    return kappa * prefactor * boltzmann


